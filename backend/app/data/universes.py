def get_core_symbols(max_symbols: int | None = None) -> list[str]:
    symbols = [item["symbol"] for item in CORE_BREADTH_UNIVERSE]
    return symbols[:max_symbols] if max_symbols else symbols


def get_symbol_sector(symbol: str) -> str:
    normalized = symbol.upper()
    match = next((item for item in CORE_BREADTH_UNIVERSE if item["symbol"] == normalized), None)
    return match["sector"] if match else "Unknown"
