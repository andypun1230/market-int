from __future__ import annotations

from dataclasses import asdict, dataclass, field
from typing import Literal

ProviderDomain = Literal["none", "quotes", "history", "quotes+history", "intelligence", "ai"]
ValidationTier = Literal["required", "optional", "manual"]


@dataclass(frozen=True)
class EndpointSpec:
    method: str
    path: str
    screen: str
    frontend_api: str
    backend_service: str
    response_model: str
    provider_domain: ProviderDomain = "none"
    required_fields: tuple[str, ...] = ()
    optional_fields: tuple[str, ...] = ()
    allowed_source_states: tuple[str, ...] = ("live", "cached", "stale", "mock", "mixed", "unavailable", "partial", "delayed", "initializing")
    partial_supported: bool = True
    live_required: bool = False
    test_required: bool = True
    tier: ValidationTier = "required"
    timeout_seconds: float = 10.0

    def model_dump(self) -> dict[str, object]:
        return asdict(self)


