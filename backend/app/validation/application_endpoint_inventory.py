def get_endpoint_inventory(*, include_report: bool = False, include_copilot: bool = False) -> list[EndpointSpec]:
    specs = list(CORE_ENDPOINTS) + list(SYMBOL_ENDPOINTS)
    if include_report:
        specs.extend(REPORT_ENDPOINTS)
    if include_copilot:
        specs.extend(COPILOT_ENDPOINTS)
    return specs


def inventory_as_dicts(*, include_report: bool = False, include_copilot: bool = False) -> list[dict[str, object]]:
    return [spec.model_dump() for spec in get_endpoint_inventory(include_report=include_report, include_copilot=include_copilot)]
